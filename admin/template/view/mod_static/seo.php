                <div class="seo_tit">什么是SEO？</div>
                <div>Search Engine Optimization(SEO),也就是搜索引擎优化，是指通过对网站结构，网页内容，
                和链接的合理优化来达到目标关键字自然排名在搜索引擎的提升，从而吸引更多的流量。</div>
            </li>
            <li>
                <div class="seo_tit">搜索引擎的排名因素是什么？</div>
                <div class="seo_tit1">网页内部因素：</div>
                <div>关键字(keyword)选择，关键字(keyword)密度　　网页标题(Title)　　网页描述(Description)　　图片alt文字　　网站结构　　文字链，外部连接　　网页内容的丰富性<br />网站规模　　网站品牌，历史   
                </div>
                <div class="seo_tit1">网页外部因素：</div>
                <div>链接的丰富性　　锚文本链接
                </div>
            </li>
            <li>
                <div class="seo_tit">如何确定关键字？</div>
                <div class="seo_tit1">选择关键词，对于新手来说，可以有以下几个要点。</div>
                <div>1、关键词不可以太热，最好搜索量在100W以下。<br />
                    2、分析用户的搜索行为来选择关键词。<br /> 	
                    3、关键词应该由访客为导向，根据用户的喜好来设定你的主关键词。<br />
                    4、关键字最好一次性确定，不要频繁修改。<br />
                </div>
            </li>
            <li>
                <div class="seo_tit">网站标题(Title)如何seo优化？</div>
                <div>1、网页的标题标签有效长度是64个字节，汉语是32个字。<br />
                    2、在网站标题里合理的融入关键词，或是关键词词组；需要注意的是：根据不同的搜索引擎，有不同的分词策略。<br /> 	
                    3、标题切忌堆积关键词，也不要一个标题优化太多关键词，一般3个关键词就差不多了。<br />
                    4、标题写法不单要考虑到seo优化，还要考虑普通用户眼球，具有营销性质的广告语也是非常好的选择。<br />
                    5、考虑标题的最终显示结果，合理安排长度；像一些CMS可以使用短标题，副标题等策略来实现。<br />
                </div>
            </li>
            <li>
                <div class="seo_tit">如何写好网站的描述标签（Description）？</div>
                <div class="seo_tit1">页面描述Description的作用有以下几点：</div>
                <div>1、简单大概描述你网站的主要内容，主要是让用户了解观看的，在百度或google搜索某个关键词的时候会看到每个搜索结果的下面都有一段描述，这段描述就是Description的内容。<br />
                    2、描述标签给搜索引擎提供了一个很好的参考，缩小了搜索引擎对网页关键词的判断范围。<br /> 	
                    3、描述标签可以适当融入关键词，从而提升关键词的竞争力。<br />
                </div>
            </li>
            <li>
                <div class="seo_tit">我们在做页面描述也需要注意：</div>
                <div>1、虽然搜索引擎对页面描述Description的作用越来越淡化，但是有描述标签肯定比没有要好，另外认真对待描述标签，能够提升网页质量。<br />
                    2、搜索引擎抓取的描述不一定就是页面的描述Description内容，特别是百度，相对谷歌更加遵循抓取规则。<br /> 	
                    3、记得不同页面使用不同的描述标签。<br />
                    4、实在精力有限管理不过来每个页面，可以通过程序实现抓取首段正文内容截取字节，或是留空。重要页面还是尽量手写。
<br />
                    5、一个误区：经常有人发现排名比自己靠前的网站竟然没描述标签，非常奇怪。解释：描述标签只是影响排名的因素之一，而排名是综合考虑的一个结果。<br />
                </div>
            </li>
        </ul>
